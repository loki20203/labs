# Завдання 1: Введення чотирьох чисел
a = float(input("Введіть перше число: "))
b = float(input("Введіть друге число: "))
c = float(input("Введіть третє число: "))
d = float(input("Введіть четверте число: "))

# Завдання 2: Арифметичні операції з першими двома числами (a і b)
results = []
results.append(a + b)      # додавання
results.append(a - b)      # віднімання
results.append(a * b)      # множення
results.append(a / b)      # ділення
results.append(a ** b)     # піднесення до ступеня
results.append(a // b)     # цілочисленне ділення
results.append(a % b)      # остача від ділення

# Виведення результатів (не обов'язково, але зручно для перевірки)
print("Результати обчислень:", results)

# Завдання 3: Кількість елементів і парні елементи
print("Кількість елементів у списку:", len(results))

# Виводимо лише парні елементи (перевіряємо, чи число ціле і чи ділиться на 2)
print("Парні елементи списку:")
for value in results:
    if isinstance(value, (int, float)) and value % 2 == 0:
        print(value)

# Завдання 4: Обмін другого і п’ятого елементів
# Перевіримо, що в списку є як мінімум 5 елементів
if len(results) >= 5:
    results[1], results[4] = results[4], results[1]

print("Список після обміну 2-го і 5-го елементів:", results)

